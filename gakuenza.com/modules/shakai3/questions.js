// helper: place a 60x60 symbol inside a figure at (cx, cy), size px
function sym(key, cx, cy, size) {
  const s = (size || 30) / 60;
  const off = (size || 30) / 2;
  return '<g transform="translate(' + (cx - off) + ' ' + (cy - off) +
    ') scale(' + s + ')">' + SYMBOL_PATHS[key] + '</g>';
}

